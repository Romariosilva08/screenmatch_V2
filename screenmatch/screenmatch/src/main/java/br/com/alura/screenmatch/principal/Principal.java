        String nomeSerieEncoded = URLEncoder.encode(nomeSerie, "UTF-8");

        // Fetch the series data using the encoded series name
        var json = consumo.obterDados(ENDERECO + nomeSerieEncoded + API_KEY);
        DadosSerie dados = conversor.obterDados(json, DadosSerie.class);
        System.out.println(dados);

        List<DadosTemporada> temporadas = new ArrayList<>();

        for (int i = 1; i <= dados.totalTemporadas(); i++) {
            json = consumo.obterDados(ENDERECO + nomeSerieEncoded + "&season=" + i + API_KEY);
            DadosTemporada dadosTemporada = conversor.obterDados(json, DadosTemporada.class);
            temporadas.add(dadosTemporada);
        }
        temporadas.forEach(System.out::println);

        //utilizaremos temporadas.get(i) para acessar as temporadas. Estaremos obtendo a temporada correspondente ao índice atual, que pode ser o índice zero, o índice um e assim por diante.
