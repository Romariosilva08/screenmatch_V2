        //temporadas.forEach(System.out::println);

        //temporadas.stream. Para pegar cada um dos episódios e aglutiná-los, utilizaremos um recurso do streams muito parecido com o .map(): o .flatMap().
        //declarar o parâmetro t e, para cada temporada, pegar t.episodios().stream(). Isso vai gerar um fluxo de dados com os episódios de todas as temporadas.
        //flatMap - faz aglutinação de lista dentro de outra
        //A função .toList() retorna uma lista imutável. Dessa forma, não conseguiremos adicionar mais elementos à lista que contém os dados de episódios.
        List<DadosEpisodio> dadosEpisodios = temporadas.stream()
                .flatMap(t -> t.episodios().stream())
                .collect(Collectors.toList());

//        System.out.println("\nTop 5 episódios");
